// symbols.js
// Строго 4096 уникальных символов. Никаких эмодзи.
const symbols = (function() {
    const arr =[];
    const ranges = [
        [0x0030, 0x0039], // Цифры (10)
        [0x0041, 0x005A], // Латиница заглавные (26)
        [0x0061, 0x007A], // Латиница строчные (26)
        [0x0410, 0x044F], // Кириллица (64)
        [0x1401, 0x1676], // Канадское слоговое письмо (630) - выглядит очень инопланетно
        [0x16A0, 0x16EA], // Руны (75)
        [0x2200, 0x22FF], // Математические операторы (256)
        [0x2500, 0x257F], // Псевдографика (128)
        [0x2800, 0x28FF], // Шрифт Брайля (256)
        [0x2C00, 0x2C2E], // Глаголица заглавные (47)
        [0x2C30, 0x2C5E], // Глаголица строчные (47)
        [0xA000, 0xA48C], // Слоги И (1165)
        [0xAC00, 0xD7A3]  // Хангыль (берем ровно столько, сколько нужно для добивки до 4096)
    ];
    
    for (let r of ranges) {
        for (let i = r[0]; i <= r[1]; i++) {
            arr.push(String.fromCodePoint(i));
            // Останавливаемся ровно на 4096 символах
            if (arr.length === 4096) return arr;
        }
    }
    return arr;
})();